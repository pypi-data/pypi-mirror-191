"""This File contains all the methods of Calculator."""


def addition(a: int, b: int):
    """Addition Method.

    Args:
        a (Int): First number
        b (int): Second Number

    Returns:
        int: sum of a and b
    """
    return a + b


def multiplication(a: int, b: int):
    """Multiplication Method.

    Args:
        a (int): Multiplicand
        b (int): Multiplier

    Returns:
        int : Multiplication of a and b
    """
    return a * b


def Subtraction(a: int, b: int):
    """Subtraction Method.

    Args:
        a (int): First Number
        b (int): Second Number

    Returns:
        int: Subtraction of a and b
    """
    return a - b


def division(a: int, b: int):
    """Division Method.

    Args:
        a (int): Dividend
        b (int): Divisor

    Returns:
        float: The result of division
    """
    return a/b
